public class Coordinates {
    // Member variables //
    private int y;
    private int x;

    // Constructor
    public Coordinates() {
        y = 0;
        x = 0;
    }

    public Coordinates(int y, int x) {
        this.y = y;
        this.x = x;
    }

    // Boring methods //
    public int getY() {
        return y;
    }

    public int getX() {
        return x;
    }
}
